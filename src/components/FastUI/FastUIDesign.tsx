import { useState, useEffect } from 'react';
import { Header, Navigation, Settings, ColorPalette, AddColorRow, Footer } from './index';
import { generatePaletteFromColor } from '../../utils/colorUtils';
import { generateStatusColors } from '../../utils/colorAlgorithm';
import '../../styles/FastUI.css';

export function FastUIDesign() {
  const [selectedScale, setSelectedScale] = useState('Linear');
  const [selectedNaming, setSelectedNaming] = useState('1,2,3...');
  
  // Состояние для выбранных цветов
  const [selectedColors, setSelectedColors] = useState({
    brand: '#E5484D',
    accent: '#3B82F6', // Статичный, без алгоритма
    info: '#0EA5E9',
    success: '#10B981',
    error: '#EF4444',
    warning: '#F59E0B',
    neutral: '#6B7280'
  });

  // Состояние для автоматически сгенерированных цветов
  const [autoGeneratedColors, setAutoGeneratedColors] = useState({
    info: '#0EA5E9',
    success: '#10B981',
    error: '#EF4444',
    warning: '#F59E0B',
    neutral: '#6B7280'
  });

  // Состояние для отслеживания ручно измененных цветов
  const [manuallyChangedColors, setManuallyChangedColors] = useState<Set<string>>(new Set());

  // Состояние для палитр оттенков
  const [colorPalettes, setColorPalettes] = useState<{[key: string]: string[]}>({});

  // Отладка состояния
  console.log('🎨 FastUIDesign состояние:', {
    selectedColors,
    colorPalettes,
    selectedScale,
    colorPalettesKeys: Object.keys(colorPalettes),
    colorPalettesLengths: Object.fromEntries(
      Object.entries(colorPalettes).map(([key, value]) => [key, value?.length || 0])
    )
  });

  // Функция для генерации автоматических цветов на основе бренд-цвета
  const generateAutoColors = (brandColor: string) => {
    const statusColors = generateStatusColors(brandColor);
    setAutoGeneratedColors(statusColors);
    
    // Обновляем выбранные цвета автоматическими (кроме brand, accent и ручно измененных)
    setSelectedColors(prev => {
      const allColors = {
        brand: brandColor,
        accent: prev.accent, // Accent остается статичным
        info: manuallyChangedColors.has('info') ? prev.info : statusColors.info,
        success: manuallyChangedColors.has('success') ? prev.success : statusColors.success,
        error: manuallyChangedColors.has('error') ? prev.error : statusColors.error,
        warning: manuallyChangedColors.has('warning') ? prev.warning : statusColors.warning,
        neutral: manuallyChangedColors.has('neutral') ? prev.neutral : statusColors.neutral
      };

      console.log('🎨 Обновляем все цвета:', allColors);

      const newPalettes: {[key: string]: string[]} = {};
      Object.entries(allColors).forEach(([type, color]) => {
        newPalettes[type] = generatePaletteFromColor(color, selectedScale === 'Semantic' ? 12 : 11, selectedScale);
      });
      setColorPalettes(newPalettes);

      return allColors;
    });
  };

  // Инициализация палитр при загрузке
  useEffect(() => {
    console.log('🔄 Инициализация палитр при загрузке');
    const initialPalettes: {[key: string]: string[]} = {};
    Object.entries(selectedColors).forEach(([type, color]) => {
      initialPalettes[type] = generatePaletteFromColor(color, selectedScale === 'Semantic' ? 12 : 11, selectedScale);
      console.log(`🎨 Палитра для ${type}:`, initialPalettes[type]);
    });
    setColorPalettes(initialPalettes);
  }, [selectedScale, selectedColors]);

  // Инициализация при загрузке
  useEffect(() => {
    console.log('🚀 Инициализация при загрузке');
    // Принудительно генерируем палитры для всех цветов
    const initialPalettes: {[key: string]: string[]} = {};
    Object.entries(selectedColors).forEach(([type, color]) => {
      initialPalettes[type] = generatePaletteFromColor(color, selectedScale === 'Semantic' ? 12 : 11, selectedScale);
    });
    setColorPalettes(initialPalettes);
    
    // Затем генерируем автоматические цвета
    generateAutoColors(selectedColors.brand);
  }, []);

  const handleColorClick = (type: string, index: number) => {
    console.log(`Clicked on ${type} palette, color index ${index}`);
  };

  const handleAddColor = () => {
    console.log('Add new color row');
  };

  const handleCopySVG = () => {
    console.log('Copy SVG');
  };

  const handleCreate = () => {
    console.log('Create');
  };

  const handleColorChange = (type: string, color: string) => {
    console.log('🎨 handleColorChange вызван:', { type, color });
    
    setSelectedColors(prev => ({
      ...prev,
      [type]: color
    }));

    // Если изменился бренд-цвет, перегенерируем автоматические цвета
    if (type === 'brand') {
      console.log('🔄 Перегенерируем автоматические цвета для бренда');
      generateAutoColors(color);
      return; // Выходим, так как generateAutoColors уже обновляет палитры
    }

    // Если изменился цвет вручную (не brand и не accent), добавляем в список ручно измененных
    if (type !== 'brand' && type !== 'accent') {
      setManuallyChangedColors(prev => new Set(prev).add(type));
    }

    // Обновляем палитру оттенков только для измененного цвета
    console.log('🔄 Обновляем палитру для:', type);
    const newPalette = generatePaletteFromColor(color, selectedScale === 'Semantic' ? 12 : 11, selectedScale);
    console.log('🎨 Новая палитра:', newPalette);
    setColorPalettes(prev => ({
      ...prev,
      [type]: newPalette
    }));
  };

  // Функция сброса к автоматическому цвету
  const handleResetToAuto = (type: string) => {
    const autoColor = autoGeneratedColors[type as keyof typeof autoGeneratedColors];
    if (autoColor) {
      setSelectedColors(prev => ({
        ...prev,
        [type]: autoColor
      }));
      
      // Убираем из списка ручно измененных
      setManuallyChangedColors(prev => {
        const newSet = new Set(prev);
        newSet.delete(type);
        return newSet;
      });
      
      // Обновляем палитру оттенков
      const newPalette = generatePaletteFromColor(autoColor, selectedScale === 'Semantic' ? 12 : 11, selectedScale);
      setColorPalettes(prev => ({
        ...prev,
        [type]: newPalette
      }));
    }
  };

  // Функция для определения индекса базового цвета в палитре
  const getBaseColorIndex = (type: string): number => {
    const color = selectedColors[type as keyof typeof selectedColors];
    const palette = colorPalettes[type];
    
    if (!palette || !color) return 5; // По умолчанию 500
    
    // Находим индекс цвета, который точно совпадает с базовым
    const exactIndex = palette.findIndex(paletteColor => paletteColor.toLowerCase() === color.toLowerCase());
    if (exactIndex !== -1) {
      return exactIndex;
    }
    
    // Если точного совпадения нет, возвращаем центр палитры
    return Math.floor(palette.length / 2);
  };

  const handleScaleChange = (scale: string) => {
    setSelectedScale(scale);
    // Перегенерируем все палитры с новым количеством оттенков
    const newPalettes: {[key: string]: string[]} = {};
    Object.entries(selectedColors).forEach(([type, color]) => {
      newPalettes[type] = generatePaletteFromColor(color, scale === 'Semantic' ? 12 : 11, scale);
    });
    setColorPalettes(newPalettes);
  };

  const paletteConfigs = [
    { type: 'brand' as const, editable: true },
    { type: 'accent' as const, editable: false },
    { type: 'info' as const, editable: true },
    { type: 'success' as const, editable: true },
    { type: 'error' as const, editable: true },
    { type: 'warning' as const, editable: true },
    { type: 'neutral' as const, editable: true },
  ];

  return (
    <div className="fast-ui-design">
      <Header />
      
      <div className="body">
        <Navigation />
        
        <div className="body-content">
          <Settings 
            selectedScale={selectedScale}
            selectedNaming={selectedNaming}
            onScaleChange={handleScaleChange}
            onNamingChange={setSelectedNaming}
          />
          
          <div className="color-schemes">
            {paletteConfigs.map((palette) => {
              console.log(`🎨 Рендеринг ${palette.type}:`, {
                selectedColor: selectedColors[palette.type],
                paletteColors: colorPalettes[palette.type],
                hasColors: colorPalettes[palette.type]?.length > 0
              });
              return (
                <ColorPalette
                  key={palette.type}
                  type={palette.type}
                  editable={palette.editable}
                  colors={colorPalettes[palette.type]}
                  selectedColor={selectedColors[palette.type]}
                  autoGeneratedColor={
                    palette.type !== 'brand' && palette.type !== 'accent'
                      ? autoGeneratedColors[palette.type as keyof typeof autoGeneratedColors]
                      : undefined
                  }
                  baseColorIndex={getBaseColorIndex(palette.type)}
                  onColorClick={(colorIndex) => handleColorClick(palette.type, colorIndex)}
                  onColorPickerClick={(color) => handleColorChange(palette.type, color)}
                  onResetToAuto={
                    palette.type !== 'brand' && palette.type !== 'accent'
                      ? () => handleResetToAuto(palette.type)
                      : undefined
                  }
                />
              );
            })}
            
            <AddColorRow 
              onAddClick={handleAddColor} 
              placeholderCount={selectedScale === 'Semantic' ? 12 : 11}
            />
          </div>
        </div>
      </div>
      
      <Footer 
        onCopySVG={handleCopySVG}
        onCreate={handleCreate}
      />
    </div>
  );
}
